func inplaceUnquote(src []byte) []byte {
	if bytes.IndexByte(src, '%') < 0 && bytes.IndexByte(src, '+') < 0 {
		// fast path: src doesn't contain encoded chars
		return src
	}
	cursor := 0
	var end = len(src)
	for i := 0; i < len(src); i++ {
		c := src[i]
		if c == '%' {
			if i+2 >= len(src) {
				src[cursor] = c
				cursor++
				continue
			}

			x2 := hex2intTable[src[i+2]]
			x1 := hex2intTable[src[i+1]]
			if x1 == 16 || x2 == 16 {
				src[cursor] = '%'
				cursor++
				end -= 2
			} else {
				src[cursor] = x1<<4 | x2
				cursor++
				i += 2
				end -= 2
			}
		} else if c == '+' {
			src[cursor] = ' '
			cursor++
		} else {
			src[cursor] = c
			cursor++
		}
	}
	return src[:end]
}

const upperhex = "0123456789ABCDEF"

func quoteArgsToBuf(v []byte, buf *[]byte) {
	for _, b := range v {
		switch {
		case b == ' ':
			*buf = append(*buf, '+')
		case quotedArgShouldEscapeTable[b] != 0:
			*buf = append(*buf, '%', upperhex[b>>4], upperhex[b&0xf])
		default:
			*buf = append(*buf, b)
		}
	}
}

func quoteHeaderValueToBuf(v []byte, buf *[]byte) {
	for _, b := range v {
		switch {
		case quoteHeaderValueEscapeTable[b] != 0:
			*buf = append(*buf, '%', upperhex[b>>4], upperhex[b&0xf])
		default:
			*buf = append(*buf, b)
		}
	}
}

func quotePathToBuf(v []byte, buf *[]byte) {
	for _, b := range v {
		switch {
		case b == ' ':
			*buf = append(*buf, '+')
		case quotedPathShouldEscapeTable[int(b)] != 0:
			*buf = append(*buf, '%', upperhex[b>>4], upperhex[b&0xf])
		default:
			*buf = append(*buf, b)
		}
	}
}
